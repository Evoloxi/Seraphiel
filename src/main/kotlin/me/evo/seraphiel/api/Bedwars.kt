@Serializable
data class Bedwars(
    val overall   : OverallBedwars   = OverallBedwars(),
    val fourFour  : FourFourBedwars  = FourFourBedwars(),
    val fourThree : FourThreeBedwars = FourThreeBedwars(),
    val eightTwo  : EightTwoBedwars  = EightTwoBedwars(),
    val eightOne  : EightOneBedwars  = EightOneBedwars(),
    val twoFour   : TwoFourBedwars   = TwoFourBedwars(),
    val castle    : CastleBedwars    = CastleBedwars(),
) {
    val bblr: Double // TODO: move this sugar to the server
        get() = if (overall.bedsLost == 0) overall.bedsBroken.toDouble() else
            overall.bedsBroken.toDouble() / overall.bedsLost.toDouble()
    val fkdr: Double
        get() = if (overall.finalDeaths == 0) overall.finalKills.toDouble() else
            overall.finalKills.toDouble() / overall.finalDeaths.toDouble()
    val wlr: Double
        get() = if (overall.losses == 0) overall.wins.toDouble() else
            overall.wins.toDouble() / overall.losses.toDouble()
}


